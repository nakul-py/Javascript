const month = 7;
// for numbers
switch (month) {
    case 1:
        console.log("January");
        break;

    case 2:
        console.log("February");
        break;

    case 3:
        console.log("March");
        break;

    case 4:
        console.log("April");
        break;
            
    default:
        console.log("Invalid month");
        break;
}

const day = "Sunday";
// for strings
switch (day) {
    case "Monday":
        console.log("Today is Monday");
        break;

    case "Tuesday":
        console.log("Today is Tuesday");
        break;

    case "Wednesday":
        console.log("Today is Wednesday");
        break;

    case "Thursday":
        console.log("Today is Thursday");
        break;

    case "Friday":
        console.log("Today is Friday");
        break;

    case "Saturday":
        console.log("Today is Saturday");
        break;

    case "Sunday":
        console.log("Today is Sunday");
        break;

    case "Holiday":
        console.log("Today is Holiday");
        break;

    default:
        console.log("Invalid day");
        break;
}